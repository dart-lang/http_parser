const int $lf = 0x0a;

/// "Carriage return" control character.
const int $cr = 0x0d;

/// Character `0`.
const int $0 = 0x30;

/// Character `9`.
const int $9 = 0x39;

/// Character `a`.
const int $a = 0x61;

/// Character `f`.
const int $f = 0x66;
